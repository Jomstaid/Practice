public class Main {
    public static void main(String[] args) {
        // Widening Casting
        int num = 10;
        double decimal = num;

        System.out.println(num);
        System.out.println(decimal);



        // Narrowing Casting
        double decimal1 = 10.501d;
        int num2 = (int) decimal1;

        System.out.println(decimal1);
        System.out.println(num2);







    }

}