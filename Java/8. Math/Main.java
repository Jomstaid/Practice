public class Main {
    public static void main(String[] args) {
        System.out.println(Math.max(1000, 1)); // maximum value
        System.out.println(Math.min(100, 1)); // minimum value
        System.out.println(Math.sqrt(52)); // square root
        System.out.println(Math.abs(-55.5)); // absolute value
        System.out.println(Math.pow(2, 3)); // power of y which is 3
        System.out.println(Math.round(5.4)); // round value
        System.out.println(Math.ceil(5.2)); // round value but small number
        System.out.println(Math.floor(4.5)); // round down
        System.out.println(Math.random()); // round value

        int ranNum = (int) (Math.random() * 101);
        System.out.println(ranNum);

    }

}